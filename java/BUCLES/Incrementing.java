/* Ejercicio: Imprimir num de 1 al 100, incrementando de a 3 num.
 
 * @author alejo
 */
public class Incrementing {
    public static void main(String[] args) {
        
        for(int i = 0; i<=100;i+=3){
            System.out.println(i);
        }
        
        
    }
    
}
